/*

Operadores aritmeticos nos sirven para hacer operaciones

    + SUMA
    - RESTA
    * MULTIPLICACION
    / DIVISION
    % MODULO

*/

let numero1= parseInt(prompt(' Ingresa el primer numero: '));
let numero2= parseInt(prompt(' Ingresa el segundo numero: '));

const suma= numero1 + numero2;
const resta= numero1 - numero2;
const multiplicacion= numero1 * numero2;
const division= numero1 / numero2;

console.log (' El primer numero es ' + numero1);
console.log (' El segundo numero es: ' + numero2);

console.log( ' El resultado de la suma es ' + suma);
console.log( ' El resultado de la resta es ' + resta);
console.log( ' El resultado de la multiplicacion es ' + multiplicacion);
console.log( ' El resultado de la division es ' + division);


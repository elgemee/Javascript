/* Operadores relacionales o de comparacion

Mayor que: a > b    a es mayor que b
Menor que: a < b    a es menor que b
Mayor o igual que: a>= b    a es mayor o igual que b
Menor o igual que: a<=b     a es menor o igual a b
Igual: ==                   a es igual a b
Distinto: !=                a es diferente a b
Operador estricto: ===
Operador diferente estricto: !==

-----------------------------------------------------------
    >       Mayor que
    <       Menor que 
    >=      Mayor o igual
    <=      Menor o igual
    ==      Igual
    !=      Distinto
    ===     Operador estricto
    !===    Operador diferente estricto
------------------------------------------------------------
*/

let numero1 = 22;
let numero2= 15;

console.log ('El primer numero es ' + numero1);
console.log ('El segundo numero es ' + numero2);

console.log(numero1 > numero2);
console.log(numero1 < numero2);
console.log(numero1 >= numero2);
console.log(numero1 <= numero2);
console.log(numero1 == numero2);
console.log(numero1 != numero2);
